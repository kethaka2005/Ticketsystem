package Ticket;

public class VIPTicket extends Ticket{

    private Double price;

    // Default constructor
    public VIPTicket() {}
    public void VIPTicket(){
        this.price=price;

    }











































































    // Parameterized constructor
    public VIPTicket( double price) {
        this.price = price;
    }

    // Getter for price
    public Double getPrice() {
        return price;
    }

    // Setter for price
    public void setPrice(Double price) {
        this.price = price;
    }
}
